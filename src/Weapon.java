public class  Weapon {
    int wand = 20;
    int sword = 15;
    int bowArrow =10;

    public Weapon(int wand, int sword, int bowArrow) {
        this.wand = wand;
        this.sword = sword;
        this.bowArrow = bowArrow;
    }

    public int getWand() {
        return wand;
    }

    public int getSword() {
        return sword;
    }

    public int getBowArrow() {
        return bowArrow;
    }


}
